function h=H(x, LM)
    
    h=zeros(LM+3,(10+2*LM));
    
    for k=0:1:LM-1
        h(k+1,1)= 2*x(1)- 2*x(5+2*k);
        h(k+1, 2)= 2*x(2)- 2*x(5+2*k+1);
        h(k+1, 5+2*k)=2*x(5+2*k)- 2*x(1);
        h(k+1, 5+2*k+1)= 2*x(5+2*k+1)- 2*x(2);
    end
    
    h(4:6, ((10+2*LM)-2):(10+2*LM))=eye(3);
    
    
end